/**
  * @brief  The application entry point.
  * @retval int
  */
int main(void)
{

  /* USER CODE BEGIN 1 */

  /* USER CODE END 1 */

  /* MCU Configuration--------------------------------------------------------*/

  /* Reset of all peripherals, Initializes the Flash interface and the Systick. */
  HAL_Init();

  /* USER CODE BEGIN Init */

  /* USER CODE END Init */

  /* Configure the system clock */
  SystemClock_Config();

  /* USER CODE BEGIN SysInit */

  /* USER CODE END SysInit */

  /* Initialize all configured peripherals */
  MX_GPIO_Init();
  MX_ADC1_Init();
  MX_TIM1_Init();
  MX_TIM3_Init();
  MX_USART2_UART_Init();
  /* USER CODE BEGIN 2 */
  previous_a = HAL_GPIO_ReadPin(GPIOB, GPIO_PIN_5);
  previous_b = HAL_GPIO_ReadPin(GPIOB, GPIO_PIN_4);
  HAL_ADCEx_Calibration_Start(&hadc1);

  HAL_TIM_PWM_Start(&htim1, TIM_CHANNEL_2);
  HAL_TIM_PWM_Start(&htim3, TIM_CHANNEL_1);

  servo_signal = 1500;
  __HAL_TIM_SET_COMPARE(&htim3,TIM_CHANNEL_1,servo_signal);

  strcpy(state,"STOP");
  /* USER CODE END 2 */

  /* Infinite loop */
  /* USER CODE BEGIN WHILE */
  while (1)
  {
    uint8_t pa10 = HAL_GPIO_ReadPin(GPIOA, GPIO_PIN_10);//switch
    uint8_t pa8  = HAL_GPIO_ReadPin(GPIOA, GPIO_PIN_8);//encoder reset

      HAL_ADC_Start(&hadc1);
      HAL_ADC_PollForConversion(&hadc1,10);
      adc_value = HAL_ADC_GetValue(&hadc1);

    //RUN/STOP button
    if(pa10 == GPIO_PIN_RESET && last_pa10_state == GPIO_PIN_SET)
    {
        RunStopFLAG = !RunStopFLAG;
    }

    //SERVO RESET button (encoder switch)
    if(pa8 == GPIO_PIN_RESET && last_pa8_state == GPIO_PIN_SET)
    {
        servo_angle = 90;
        servo_reset_flag = 1;
    }

      last_pa10_state = pa10;
      last_pa8_state  = pa8;

      if(RunStopFLAG)
      {
          strcpy(state,"RUN");

          duty_percentage = (adc_value * 100)/4095;//convert adc to percentage to LED
          pwm_value = (adc_value * htim1.Init.Period)/4095;//convert to pwm value
          __HAL_TIM_SET_COMPARE(&htim1, TIM_CHANNEL_2,pwm_value);//give pwm value to led
      }
      else
      {
          strcpy(state,"STOP");
          duty_percentage = 0; //resist uatrt to print old duty value
          __HAL_TIM_SET_COMPARE(&htim1,TIM_CHANNEL_2,0);//turn off pwm for led
      }

      //servo
      current_a = HAL_GPIO_ReadPin(GPIOB, GPIO_PIN_5);
      current_b = HAL_GPIO_ReadPin(GPIOB, GPIO_PIN_4);

     if(current_a != previous_a)
     {
         if(current_a == GPIO_PIN_SET)
         {
             if(current_b != current_a)
                 servo_angle++;
             else
                 servo_angle--;
         }
     if(servo_angle > 180) servo_angle = 180;
     if(servo_angle < 0) servo_angle = 0;
     }
     previous_a = current_a;

    servo_signal = 1000 + (servo_angle * 1000) / 180;
    __HAL_TIM_SET_COMPARE(&htim3, TIM_CHANNEL_1, servo_signal);

      if(HAL_UART_Receive(&huart2,&rx_char,1,0) == HAL_OK)
        {
            if(rx_char == 'R')
            {
                RunStopFLAG = 1;
                HAL_UART_Transmit(&huart2,(uint8_t*)"CMD: RUN\r\n",10,100);
            }

            else if(rx_char == 'S')
            {
                RunStopFLAG = 0;
                HAL_UART_Transmit(&huart2,(uint8_t*)"CMD: STOP\r\n",11,100);
            }

            else if(rx_char == 'C')
            {
                servo_angle = 90;
                HAL_UART_Transmit(&huart2,(uint8_t*)"CMD: CENTRE\r\n",13,100);
            }
        }

       //UART PRINTING
      if(HAL_GetTick() - last_uart_tick >= 500)
      {
          last_uart_tick=HAL_GetTick();
          sprintf(uart_msg,"ADC:%lu | Duty:%lu | State:%s | Servo:%ld\r\n",adc_value,duty_percentage,state,servo_angle);
          HAL_UART_Transmit(&huart2,(uint8_t*)uart_msg,strlen(uart_msg),100);
      }
      if(HAL_GetTick() - last_heartbeat_tick >= 500)
      {
          last_heartbeat_tick = HAL_GetTick();
          HAL_GPIO_TogglePin(GPIOA,GPIO_PIN_5);
      }
    /* USER CODE END WHILE */

    /* USER CODE BEGIN 3 */
  }
  /* USER CODE END 3 */
}

